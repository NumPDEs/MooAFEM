               'mu'            , '$|| f - \Pi_0 f ||_{L^2(\Omega)}$',...
               'est'           , 'built-in',...
               'res'           , '$LS(f; \sigma_\ell, u_\ell)$',...
               'resRed'        , '$LS(\Pi_0 f; \sigma_\ell, u_\ell)$',...
               'resDiv'        , '$|| f + \textrm{div} \sigma_\ell ||_{L^2(\Omega)}$',...
               'resL2'         , '$|| \sigma_\ell - \nabla u_\ell ||_{L^2(\Omega)}$',...
               'resDev'        , '$|| \textrm{dev} \sigma_\ell - \textrm{D} u_\ell ||_{L^2(\Omega)}$',...
               'resMat'        , '$|| C^{-1} \sigma_\ell - \varepsilon u_\ell ||_{L^2(\Omega)}$',...
               'errU'          , '$|| u - u_\ell ||_{H^1(\Omega)}$',...
               'errL2U'        , '$|| u - u_\ell ||_{L^2(\Omega)}$',...
               'errGradU'      , '$||| u - u_\ell |||$',...
               'errSigma'      , '$|| \sigma - \sigma_\ell ||_{H(\textrm{div},\Omega)}$',...
               'errL2Sigma'    , '$|| \sigma - \sigma_\ell ||_{L^2(\Omega)}$',...
               'errDivSigma'   , '$|| \textrm{div} (\sigma - \sigma_\ell) ||_{L^2(\Omega)}$',...
               'runtime'       , 'runtime',...
               'setupTime'     , 'setuptime',...
               'solutionTime'  , 'solutiontime',...
               'estimatorTime' , 'estimatortime',...
               'refinementTime', 'refinementtime',...
               'condition'     , 'condition');
    % Dictionary for the latex description of data in the convergence
    % history plot for octave
    DICTIONARY_OCTAVE =...
        struct('eta'           , '\eta_{l}',...
               'etaVol'        , '\eta_{{vol,l}}',...
               'etaJump'       , '\eta_{{jump,l}}',...
               'oscDb'         , '{osc}({D} u_{{D}}, F_l(\Gamma_{{D}}))',...
               'oscNb'         , '{osc}(t_{{N}}, F_l(\Gamma_{{N}}))',...
               'oscF'          , '{osc}(f, T_\ell)',...
               'mu'            , '|| f - \Pi_0 f ||_{L^2(\Omega)',...
               'est'           , 'built-in',...
               'res'           , 'LS(f; \sigma_l, u_l)',...
               'resRed'        , 'LS(\Pi_0 f; \sigma_l, u_l)',...
               'resDiv'        , '|| f + {div} \sigma_l ||_{L^2(\Omega)}',...
               'resL2'         , '|| \sigma_l - \nabla u_l ||_{L^2(\Omega)}',...
               'resDev'        , '|| {dev} \sigma_l - {D} u_l ||_{L^2(\Omega)}',...
               'resMat'        , '|| C^{-1} \sigma_l - \epsilon u_l ||_{L^2(\Omega)}',...
               'errU'          , '|| u - u_l ||_{H^1(\Omega)}',...
               'errL2U'        , '|| u - u_\ell ||_{L^2(\Omega)}',...
               'errGradU'      , '||| u - u_\ell |||',...
               'errSigma'      , '|| \sigma - \sigma_l ||_{H({div},\Omega)}',...
               'errL2Sigma'    , '|| \sigma - \sigma_\ell ||_{L^2(\Omega)}',...
               'errDivSigma'   , '|| {div} (\sigma - \sigma_\ell) ||_{L^2(\Omega)}',...
               'runtime'       , 'runtime',...
               'setupTime'     , 'setuptime',...
               'solutionTime'  , 'solutiontime',...
               'estimatorTime' , 'estimatortime',...
               'refinementTime', 'refinementtime',...
               'condition'     , 'condition');
    % Choose dictionary according to running program
    if isOctave()
        DICTIONARY = DICTIONARY_OCTAVE;
    else
        DICTIONARY = DICTIONARY_MATLAB;
    end
end