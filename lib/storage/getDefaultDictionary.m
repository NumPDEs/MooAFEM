    % convergence history plot
    DICTIONARY_MATLAB =...
        struct('eta'           , '$\eta_\ell$',...
               'etaVol'        , '$\eta_{\textrm{vol}}$',...
               'etaJump'       , '$\eta_{\textrm{jump}}$',...
               'oscDb'         , '$\textrm{osc}(\textrm{D} u_{\textrm{D}}, \mathcal{F}_\ell(\Gamma_{\textrm{D}})$',...
               'oscNb'         , '$\textrm{osc}(t_{\textrm{N}}, \mathcal{F}_\ell(\Gamma_{\textrm{N}})$',...
